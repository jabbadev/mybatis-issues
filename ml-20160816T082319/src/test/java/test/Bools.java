package test;

public class Bools {
  private Integer id;
  private boolean yn;
  private boolean onezero;
  private boolean tf;

  public Integer getId() {
    return id;
  }

  public void setId(Integer id) {
    this.id = id;
  }

  public boolean isYn() {
    return yn;
  }

  public void setYn(boolean yn) {
    this.yn = yn;
  }

  public boolean isOnezero() {
    return onezero;
  }

  public void setOnezero(boolean onezero) {
    this.onezero = onezero;
  }

  public boolean isTf() {
    return tf;
  }

  public void setTf(boolean tf) {
    this.tf = tf;
  }
}
